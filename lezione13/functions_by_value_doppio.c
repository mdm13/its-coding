#include <stdio.h>
void doppio(int *n) {
    printf("n=%d\n",*n);
    (*n)*=2;
    printf("n=%d\n",*n);
}

int main()
{
    int i=2;

    printf("i=%d\n",i);
    doppio(&i);
    printf("i=%d\n",i);
    
    return 0;
}

